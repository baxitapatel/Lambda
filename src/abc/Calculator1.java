package abc;

public class Calculator1 {

    // public access specifier
    public int add(int a , int b){
        int add = a+b;
        return add;
    }
    // default access specifier

    int subtraction (int a,int b){
        int subtraction = a - b;
        return subtraction;
    }
    double divide  (double a , double b){
        double divide = a/b;
        return divide;
    }
}
